package cn.xy.bean;

public class Operator {

    private int operator_id;
    private String operator_account;
    private String operator_pwd;
    private String operator_type;
    private String operator_status;

    public int getOperator_id() {
        return operator_id;
    }

    public void setOperator_id(int operator_id) {
        this.operator_id = operator_id;
    }

    public String getOperator_account() {
        return operator_account;
    }

    public void setOperator_account(String operator_account) {
        this.operator_account = operator_account;
    }

    public String getOperator_pwd() {
        return operator_pwd;
    }

    public void setOperator_pwd(String operator_pwd) {
        this.operator_pwd = operator_pwd;
    }

    public String getOperator_type() {
        return operator_type;
    }

    public void setOperator_type(String operator_type) {
        this.operator_type = operator_type;
    }

    public String getOperator_status() {
        return operator_status;
    }

    public void setOperator_status(String operator_status) {
        this.operator_status = operator_status;
    }
}
